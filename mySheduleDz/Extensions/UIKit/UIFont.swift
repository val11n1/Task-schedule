import UIKit

extension UIFont {
    
    //MARK: Avenir Next
    
    static func avenirNext18() -> UIFont? {
        
        return UIFont.init(name: "Avenir Next", size: 18)
    }
    
    static func avenirNext14() -> UIFont? {
        
        return UIFont.init(name: "Avenir Next", size: 14)
    }
    
    //MARK: Avenir Next Demi Bold
    
    static func avenirNextDemiBold18() -> UIFont? {
        
        return UIFont.init(name: "Avenir Next Demi Bold", size: 18)
    }
    
    static func avenirNextDemiBold14() -> UIFont? {
        
        return UIFont.init(name: "Avenir Next Demi Bold", size: 14)
    }
}
